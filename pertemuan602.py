import math

#Menghitung akar kuadrat dari x
print (math.sqrt(25))

#Menghitung x^y
print (math.pow(4,5))

#Menghitung Nilai Absolut (mutlak)
print (math.fabs(-8))

#Menghitung Faktorial
print (math.factorial(10))

#Menghitung Nilai Pembulatan Keatas
print (math.ceil(5.8))

#Menghitung Nilai Pemulatan Kebawah
print (math.floor(4.3))
