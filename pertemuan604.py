print ("Menghitung Luas dan Keliling Lingkaran")

#Input nilai jari-jari
r = float (input("Masukan Nilai Jari-Jari (r): "))

#Nilai Phi
phi = 3.14

#Rumus
luas = phi * r * r
kll = 2 * phi * r

#Proses hitung nilai Luas dan keliling
print ("Luas = 3.14 x r x r")
print ("Luas = ",luas)
print ("Keliling = 2 x 3.14 x r")
print ("Keliling = ",kll)

